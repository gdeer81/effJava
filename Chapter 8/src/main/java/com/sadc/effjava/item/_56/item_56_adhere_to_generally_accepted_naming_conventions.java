public class item_56_adhere_to_generally_accepted_naming_conventions {
    public static void main(String[] args) {
        System.out.println("package names should be the name of the organization's domain name, ex. 'com.heb'");
        System.out.println("package names should be hierarchical and descriptive");
        System.out.println("class and interface names should be one or more words in CamelCase" +
                           " and abbreviations/acronyms should be avoided");
        System.out.println("constant fields in classes should be UPPERCASE");
        System.out.println("class methods that do things should be named a verb or verb phrase");
        System.out.println("getters and setters should be named 'getX' and 'setX'");
    }
}